#Belajar Aritmatika
# penjumlahan sama dengan +
# pengurangan sama dengan -
# perkalian sama dengan *
# pembagian sama dengan /
# modulo/modulus ( % ) digunakan untuk menentukan sisa bagi
# perpangkatan = **

angka1 = 5
angka2 = 2

# penjumlahan bilangan
hasil = angka1 + angka2
print("hasil penjumlahannya adalah = ", hasil)

#pengurangan bilangan
hasil= angka1 - angka2
print("hasil pengurangan adalah = ", hasil)

#perkalian bilangan
hasil= angka1 * angka2
print("hasil perkalian adalah=",hasil)

#pembagian bilangan
hasil= angka1 / angka2
print("hasil pembagian adalah =",hasil)

# modulus
hasil= angka1 % angka2
print("sisa baginya adalah =", hasil)

#perpangkatan
hasil= angka1 ** angka2
print("hasil perpangkatan", angka1,"adalah=", hasil)