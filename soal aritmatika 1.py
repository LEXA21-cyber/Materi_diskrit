# Sebuah persegi panjang memiliki panjang 20cm dan lebar 15cm, tentukan luas persegi panjang tersebut

angka1 =20
angka2 =15

#luas persegi panjang
hasil= angka1 * angka2
print("Sebuah persegi panjang memiliki panjang 20cm dan lebar 15cm, tentukan luas persegi panjang tersebut")
print("penjang = 20 cm")
print("lebar = 15cm")
print("panjang persegi = P x L")
print("luas persegi panjang adalah =", hasil,"cm")

